/*
Задание 2. Задан массив из n целых чисел, которые нужно ввести с 
клавиатуры (число n также вводится с клавиатуры, его значение не больше 20). 
Определить, является ли упорядоченной по возрастанию часть массива, 
включающая элементы с номерами от n1 до n2. n1, n2 вводятся с клавиатуры. 
Программа должна вывести «да» или «нет». Сделать проверку на корректность
ввода n.
*/

#include <iostream>

int main() 
{
    int n, n1, n2;
    std::cout << "Введите число n (не более 20): ";
    std::cin >> n;

    if (n <= 0 || n > 20) 
    {
        std::cout << "Ошибка: введено некорректное значение n." << std::endl;
        return 1;
    }

    int A[20];  // Объявляем массив A размера 20 (максимальный размер)
    
    // Ввод массива с клавиатуры
    std::cout << "Введите элементы массива:" << std::endl;
    for (int i = 0; i < n; ++i) 
    {
        std::cin >> A[i];
    }

    std::cout << "Введите значения n1 и n2: ";
    std::cin >> n1 >> n2;

    if (n1 < 0 || n1 >= n || n2 <= n1 || n2 >= n) 
    {
        std::cout << "Ошибка: некорректные значения n1 и n2." << std::endl;
        return 1;
    }

    // Проверяем упорядоченность части массива от n1 до n2
    bool ordered = true;
    for (int i = n1; i < n2; ++i) 
    {
        if (A[i] > A[i + 1]) 
        {
            ordered = false;
            break;
        }
    }

    // Выводим результат
    if (ordered) 
    {
        std::cout << "Да, упорядоченная по возрастанию." << std::endl;
    } 
    else 
    {
        std::cout << "Нет, не упорядоченная по возрастанию." << std::endl;
    }

    return 0;
}