/*
Задание 1. Сформировать массив A из n случайных целых чисел в диапазоне
от -10 до 10 (число n вводится с клавиатуры, его значение не больше 20). Найти
сумму и количество всех элементов с нечётными номерами. Сделать проверку на
корректность ввода n.
*/

#include <iostream>
#include <random>
#include <vector>

int main() 
{
    int n;
    
    do 
    {
        std::cout << "Введите значение n (не больше 20): ";
        std::cin >> n;

        // Проверка корректности ввода n
        if (n <= 0 || n > 20) 
        {
            std::cout << "Некорректное значение n. Пожалуйста, введите число от 1 до 20." << std::endl;
        }
    } 
    while (n <= 0 || n > 20);

    // Инициализация генератора случайных чисел
    std::random_device rd;
    std::mt19937 gen(rd());
    std::uniform_int_distribution<> dist(-10, 10);

    // Создание и заполнение массива A случайными числами
    std::vector<int> A(n);
    for (int i = 0; i < n; ++i) 
    {
        A[i] = dist(gen);
    }

    // Вычисление суммы и количества элементов с нечетными номерами
    int sum = 0;
    int count = 0;
    for (int i = 0; i < n; ++i) 
    {
        if (i % 2 != 0) 
        {
            sum += A[i];
            count++;
        }
    }

    // Вывод результатов
    std::cout << "\nМассив A: ";
    for (int i = 0; i < n; ++i) 
    {
        std::cout << A[i] << " ";
    }
    
    std::cout << std::endl;
    std::cout << "\nСумма элементов с нечетными номерами: " << sum << std::endl;
    std::cout << "Количество элементов с нечетными номерами: " << count << std::endl;

    return 0;
}
