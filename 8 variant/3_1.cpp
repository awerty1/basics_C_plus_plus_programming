/*
Задание 1. Сформировать массив A из n случайных целых чисел в диапазоне
от -50 до 50 (число n вводится с клавиатуры, его значение не больше 20). Найти
количество элементов с четными номерами и определить наименьший среди них.
Сделать проверку на корректность ввода n 
*/

#include <iostream>
#include <cstdlib>
#include <ctime>
#include <limits> // Для очистки ввода

int main() 
{
    int n;
    int A[20];

    do 
    {
        // Запросить значение n
        std::cout << "Введите количество элементов (n <= 20): ";

        // Проверка корректности ввода и обработка исключений
        while (!(std::cin >> n)) 
        {
            std::cout << "Ошибка! Пожалуйста, введите целое число." << std::endl;
            std::cin.clear(); // Очистить флаг ошибки ввода
            std::cin.ignore(std::numeric_limits<std::streamsize>::max(), '\n'); // Очистить буфер ввода
        }

        if (n > 20 || n <= 0) 
        {
            std::cout << "Некорректный ввод. Пожалуйста, введите число от 1 до 20." << std::endl;
        }
    } 
    while (n > 20 || n <= 0);

    // Заполнить массив A случайными числами
    srand(time(0));
    std::cout << "Элементы массива A: ";
    for (int i = 0; i < n; i++) 
    {
        A[i] = rand() % 101 - 50;
        std::cout << A[i] << " ";
    }
    std::cout << std::endl;

    // Найти количество элементов с четными номерами и определить наименьший среди них
    int count = n / 2;
    int min_even = A[0]; // Переменная для хранения наименьшего числа с четным индексом

    for (int i = 0; i < n; i += 2) 
    {
        if (A[i] < min_even) 
        {
            min_even = A[i];
        }
    }

    // Вывести результат
    std::cout << "Количество элементов с четными номерами: " << count << std::endl;
    std::cout << "Наименьший элемент среди них: " << min_even << std::endl;

    return 0;
}
