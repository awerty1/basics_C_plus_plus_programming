/*
Задание 3. Задан массив из n случайных целых чисел (число n вводится с
клавиатуры, его значение не больше 20). Удалить все отрицательные элементы,
если таковые имеются, и вывести измененный массив на экран. Дополнительные
массивы использовать нельзя – меняем исходный массив с помощью сдвига
элементов влево. Пример приведен в методичке. Сделать проверку на
корректность ввода n.
*/

#include <iostream>
#include <vector>
#include <limits>

int main()
{
    int n;
    
    // Ввод размера массива с защитой от некорректного ввода
    do
    {
        std::cout << "Введите размер массива (n не больше 20): ";
        std::cin >> n;
        
        if (std::cin.fail() || n <= 0 || n > 20)
        {
            std::cout << "Пожалуйста, введите целое число в диапазоне от 1 до 20.\n";
            std::cin.clear(); // Сброс флага ошибки ввода
            std::cin.ignore(std::numeric_limits<std::streamsize>::max(), '\n'); // Очистка ввода
        }
    } 
    while (n <= 0 || n > 20);

    std::vector<int> arr(n);

    // Ввод элементов массива
    std::cout << "Введите " << n << " целых чисел для массива: " << std::endl;
    for (int i = 0; i < n; ++i)
    {
        size_t inc1 = i+1;
        std::cout << "Ввведите ARR[" << inc1 <<"]:"; 
        std::cin >> arr[i];
        
        if (std::cin.fail()) 
        {
            std::cout << "Пожалуйста, введите целое число.\n";
            std::cin.clear(); // Сброс флага ошибки ввода
            std::cin.ignore(std::numeric_limits<std::streamsize>::max(), '\n'); // Очистка ввода
            i--; // Повторяем ввод для текущего элемента
        }
    }

    // Удаление всех отрицательных элементов из исходного массива
    int shift = 0;
    for (int i = 0; i < n; ++i)
    {
        if (arr[i] >= 0)
        {
            arr[i - shift] = arr[i];
        }
        else
        {
            shift++;
        }
    }

    // Вывод измененного массива на экран
    std::cout << "Измененный массив без отрицательных элементов: " << std::endl;
    for (int i = 0; i < n - shift; ++i)
    {
        std::cout << arr[i] << " ";
    }
    std::cout << std::endl;

    return 0;
}
